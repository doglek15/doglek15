// Membuat fungsi sederhana untuk menghitung jumlah dua angka
function tambah(a, b) {
    return a + b;
}

// Memanggil fungsi tambah dan menampilkan hasilnya di konsol
console.log("Hasil penjumlahan 3 + 5 adalah: " + tambah(3, 5));

// Membuat array sederhana dan menampilkan isi array di konsol
var fruits = ["Apple", "Banana", "Orange"];
console.log("Buah-buahan: " + fruits.join(", "));

// Membuat objek sederhana
var person = {
    name: "John",
    age: 30,
    city: "New York"
};

// Menampilkan properti objek di konsol
console.log("Nama: " + person.name);
console.log("Usia: " + person.age);
console.log("Kota: " + person.city);
